public class Constantes_video_14 {
    public static void main(String[] args) {
        System.out.println("--------------Uso de las constantes en java -------");

        //Para usar constante no usas const como en otro lenguajes sino que
        //Usas la palabra reservada
        //final + tipo + nombre_de_variable
        //Un ejemplo concreto de esto es

        final double Pi = 3.14;

        System.out.println(Pi);


        System.out.println(Pi);

    }
}

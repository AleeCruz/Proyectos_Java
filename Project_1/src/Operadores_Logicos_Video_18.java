public class Operadores_Logicos_Video_18 {
    public static void main(String[] args) {

        boolean a = true, b=false;


        //Operadores logicos and

        System.out.println(a && b);

        //Operador logico or
        System.out.println( a || b);

        //Operador Logico not
        System.out.println(!a);

        //Operador Logico Xor
        System.out.println(a^b);


    }
}
